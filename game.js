function create_map(map) {
    let divh = ''
    for (let i = 0; i < 11; i++) {
        let divw = ''
        for (let j = 0; j < 11; j++) {
            divw += "<div id ='"+map[i][j]+"' class = 'w'></div>"
        }
        divh += "<div>" + divw + "</div>"
    }
    document.getElementById('container').innerHTML = divh
}


